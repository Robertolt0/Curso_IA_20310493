#Capitulo 21 El condicional IF y operadores de comparación

#Cambia el operador para que la condición sea True
num1 = 15
num2 = 20

if num1 < num2:
	print('Se ejecuta el if Operación 1.')
	
#Cambia el operador para que la condición sea True.
num1 = 1450
num2 = 60

if num1 > num2:
	print('Se ejecuta el if Operación 2.')
	
#Haz que el siguiente condicional se convierta en False sin cambiar el operador.
num1 = 60
num2 = 60

if num1 != num2:
	print('Se ejecuta el if Operación 3.')
