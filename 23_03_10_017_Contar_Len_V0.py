#Capitulo 18 Contar elementos con len()
'''El meotodo lent nos ayudara a contar los elementos de una lista
Si la lista es modificada el valor de lent tambien se ve afectado
'''
#Este capitulo no contiene ejercicios
paises=['Portugal', 'Suiza', 'Alemania', 'Francia', 'Belgica', 'Chile']
